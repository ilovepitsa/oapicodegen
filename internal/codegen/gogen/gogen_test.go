func TestCreate_GofmtApplied(t *testing.T) {
	ff := NewFileFactory("oapigen")
	// Намеренно «плохой» отступ — gofmt должен нормализовать
	body := []byte("func main(){\nif true {\nreturn\n}\n}\n")
	f := ff.Create(&File{Package: "main", Body: body})
	got := string(f.Content())

	assert.Contains(t, got, "\tif true {", "expected gofmt-normalized indentation; got:\n%s", got)
}

func TestCreate_InvalidGoFallback(t *testing.T) {
	ff := NewFileFactory("oapigen")
	body := []byte("this is not valid go code !!!")
	f := ff.Create(&File{Package: "main", Body: body})
	got := string(f.Content())

	assert.Contains(t, got, "this is not valid go code !!!", "expected fallback raw content")
	assert.Contains(t, got, "// Code generated by oapigen, DO NOT EDIT.", "expected header in fallback")
}

