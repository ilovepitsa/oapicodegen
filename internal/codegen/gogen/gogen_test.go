func TestNewFileFactory_ToolName(t *testing.T) {
	ff := NewFileFactory("oapigen")
	assert.Equal(t, "oapigen", ff.toolName)
}

func TestCreate_HeaderAndPackage(t *testing.T) {
	ff := NewFileFactory("oapigen")
	f := ff.Create(&File{Package: "model", Body: []byte("")})
	got := string(f.Content())

	want := "// Code generated by oapigen, DO NOT EDIT.\n\npackage model\n"
	assert.Equal(t, want, got)
}

func TestCreate_WithBody(t *testing.T) {
	ff := NewFileFactory("oapigen")
	f := ff.Create(&File{Package: "main", Body: []byte("var x = 1\n")})
	got := string(f.Content())

	assert.Contains(t, got, "var x = 1", "expected body; got:\n%s", got)
	assert.Contains(t, got, "package main", "expected package decl; got:\n%s", got)
}

func TestCreate_ImportsSorted(t *testing.T) {
	ff := NewFileFactory("oapigen")
	f := ff.Create(&File{
		Package: "model",
		Imports: []Import{
			{Path: "fmt"},
			{Path: "os"},
			{Path: "bytes"},
		},
		Body: []byte("var _ = fmt.Sprintf\nvar _ = os.Args\nvar _ = bytes.NewBuffer\n"),
	})
	got := string(f.Content())

	assert.Contains(t, got, `"bytes"`)
	assert.Contains(t, got, `"fmt"`)
	assert.Contains(t, got, `"os"`)
	bytesIdx := strings.Index(got, `"bytes"`)
	fmtIdx := strings.Index(got, `"fmt"`)
	osIdx := strings.Index(got, `"os"`)
	assert.True(t, bytesIdx < fmtIdx && fmtIdx < osIdx, "imports not sorted; got bytes=%d, fmt=%d, os=%d", bytesIdx, fmtIdx, osIdx)
}

